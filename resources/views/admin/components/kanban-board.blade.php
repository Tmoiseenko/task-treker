@props([
    'components' => [],
])

{{--
    Горизонтальный скролл требует ДВУХ отдельных слоёв:

    ┌── ВНЕШНИЙ (блочный, ширина = 100% родителя) ─────────────────────────────┐
    │   overflow-x-auto + фиксированная высота                                  │
    │   ┌── ВНУТРЕННИЙ (inline-flex, растёт с контентом) ─────────────────────┐│
    │   │   inline-flex h-full gap-4                                           ││
    │   │   [колонка flex-none w-80] [колонка flex-none w-80] ...              ││
    │   └──────────────────────────────────────────────────────────────────────┘│
    └───────────────────────────────────────────────────────────────────────────┘

    Почему inline-flex, а не flex:
    • flex-контейнер = блочный → ширина ограничена родителем → не может быть шире
      → overflow-x-auto не может создать скролл, т.к. контент не "выходит за края"
    • inline-flex = инлайновый → РАСТЁТ с контентом → становится шире родителя
      → overflow-x-auto на внешнем div видит переполнение → создаёт скроллбар

    Почему body{overflow-x:hidden} не ломает это:
    • MoonShine ставит overflow-x:hidden на body, но скролл происходит на ВНЕШНЕМ div,
      а не на body — браузер рисует скроллбар внутри div, body не затронут.

    Высота calc(100dvh - 12rem):
    • 12rem ≈ topbar(4) + breadcrumbs(3) + заголовок страницы(3) + padding(2)
    • Подбирается под конкретный layout; передайте style через $attributes для переопределения
--}}

{{-- Внешний: блочный элемент с ограниченной шириной + горизонтальный скролл --}}
<div {{ $attributes->merge([
    'class' => 'overflow-x-auto pb-4',
    'style' => 'height: calc(100dvh - 15rem);',
]) }}>

    {{-- Внутренний: inline-flex растёт вместе с контентом, заполняет всю высоту --}}
    <div class="inline-flex gap-4 h-full">
        <x-moonshine::components :components="$components" />

        {{ $slot ?? '' }}
    </div>

</div>

